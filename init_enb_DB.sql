CREATE EXTENSION IF NOT EXISTS postgis;

CREATE TABLE IF NOT EXISTS legacy_user (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type SMALLINT NOT NULL,
    username VARCHAR(255) NOT NULL UNIQUE,
    auth_key VARCHAR(32) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    password_reset_token VARCHAR(255) UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    currency VARCHAR(5) NOT NULL,
    name VARCHAR(255) NOT NULL,
    address VARCHAR(255) NOT NULL,
    phone VARCHAR(255) NOT NULL,
    inn VARCHAR(20) NOT NULL,
    status SMALLINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    location geometry(Point,4326),
    contacts TEXT NOT NULL
);

CREATE INDEX IF NOT EXISTS legacy_user_location_gix ON legacy_user USING GIST (location);

CREATE TABLE IF NOT EXISTS catalog (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR(255) NOT NULL UNIQUE,
    active SMALLINT NOT NULL
);

CREATE TABLE IF NOT EXISTS categories (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    catalog INTEGER NOT NULL REFERENCES catalog(id) ON DELETE RESTRICT,
    title VARCHAR(255) NOT NULL UNIQUE,
    active SMALLINT NOT NULL
);

CREATE TABLE IF NOT EXISTS advert (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type SMALLINT NOT NULL,
    category INTEGER NOT NULL REFERENCES categories(id) ON DELETE RESTRICT,
    author INTEGER NOT NULL REFERENCES legacy_user(id) ON DELETE RESTRICT,
    address VARCHAR(255),
    location geometry(Point,4326) NOT NULL,
    delivery BOOLEAN NOT NULL DEFAULT FALSE,
    contacts TEXT NOT NULL,
    title VARCHAR(255) NOT NULL,
    text TEXT NOT NULL,
    price DOUBLE PRECISION NOT NULL,
    wholesale_price DOUBLE PRECISION NOT NULL,
    min_volume DOUBLE PRECISION NOT NULL,
    wholesale_volume DOUBLE PRECISION NOT NULL,
    volume DOUBLE PRECISION NOT NULL,
    priority INTEGER NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    status SMALLINT NOT NULL
);

CREATE INDEX IF NOT EXISTS advert_location_gix ON advert USING GIST (location);
CREATE INDEX IF NOT EXISTS advert_status_idx ON advert(status);
CREATE INDEX IF NOT EXISTS advert_category_idx ON advert(category);
CREATE INDEX IF NOT EXISTS advert_author_idx ON advert(author);

CREATE TABLE IF NOT EXISTS advert_photo (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    advert_id INTEGER NOT NULL REFERENCES advert(id) ON DELETE CASCADE,
    image VARCHAR(512) NOT NULL,
    sort INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS advert_photo_advert_sort_idx ON advert_photo(advert_id, sort);

CREATE TABLE IF NOT EXISTS file (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type SMALLINT NOT NULL,
    name VARCHAR(255) NOT NULL UNIQUE,
    author INTEGER NOT NULL REFERENCES legacy_user(id) ON DELETE RESTRICT,
    about VARCHAR(255) NOT NULL,
    size INTEGER NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    status SMALLINT NOT NULL
);

CREATE INDEX IF NOT EXISTS file_author_idx ON file(author);

CREATE TABLE IF NOT EXISTS page (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type SMALLINT NOT NULL,
    author INTEGER NOT NULL REFERENCES legacy_user(id) ON DELETE RESTRICT,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    address VARCHAR(255) NOT NULL UNIQUE,
    title VARCHAR(255) NOT NULL,
    text TEXT,
    status SMALLINT NOT NULL
);

CREATE INDEX IF NOT EXISTS page_author_idx ON page(author);

CREATE TABLE IF NOT EXISTS review (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    type SMALLINT NOT NULL,
    "object" INTEGER NOT NULL,
    points INTEGER NOT NULL,
    author INTEGER NOT NULL REFERENCES legacy_user(id) ON DELETE RESTRICT,
    text TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    status SMALLINT NOT NULL
);

CREATE INDEX IF NOT EXISTS review_author_idx ON review(author);
CREATE INDEX IF NOT EXISTS review_object_idx ON review("object");

CREATE TABLE IF NOT EXISTS seller (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    "user" INTEGER NOT NULL REFERENCES legacy_user(id) ON DELETE RESTRICT,
    name VARCHAR(255) NOT NULL UNIQUE,
    logo INTEGER NOT NULL,
    location TEXT NOT NULL,
    contacts JSONB NOT NULL,
    price_list INTEGER NOT NULL,
    links TEXT NOT NULL,
    about TEXT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    status SMALLINT NOT NULL
);

CREATE INDEX IF NOT EXISTS seller_user_idx ON seller("user");

CREATE TABLE IF NOT EXISTS variable (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    value TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS migration (
    version VARCHAR(180) PRIMARY KEY,
    apply_time INTEGER
);
