def _build_regions():
    regions = {}
    for name, prepositional, min_lon, min_lat, max_lon, max_lat in _REGIONS_RAW:
        slug = slugify_ru(name)
        regions[slug] = {
            'name': name,
            'slug': slug,
            'prepositional': prepositional,
            'bbox': (min_lon, min_lat, max_lon, max_lat),
        }
    return regions


